package com.evertecinc.b2c.enex.client.exceptions;

public class ReporteException extends Exception{
	
	private static final long serialVersionUID = -6340607844054298787L;

	public ReporteException() {
	}

	public ReporteException(String arg0) {
		super(arg0);
	}

	public ReporteException(Throwable arg0) {
		super(arg0);
	}

	public ReporteException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
