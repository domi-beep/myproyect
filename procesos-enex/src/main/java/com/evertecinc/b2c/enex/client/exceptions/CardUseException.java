package com.evertecinc.b2c.enex.client.exceptions;

public class CardUseException extends Exception{

		
		/**
	 * 
	 */
	private static final long serialVersionUID = 7343853052714188041L;

		/**
		 * Serializable
		 */


		public CardUseException() {
		}

		public CardUseException(String arg0) {
			super(arg0);
		}

		public CardUseException(Throwable arg0) {
			super(arg0);
		}

		public CardUseException(String arg0, Throwable arg1) {
			super(arg0, arg1);
		}

	

}
