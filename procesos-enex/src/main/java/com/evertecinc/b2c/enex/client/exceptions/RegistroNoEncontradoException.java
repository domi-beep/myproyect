package com.evertecinc.b2c.enex.client.exceptions;

public class RegistroNoEncontradoException extends Exception{
	
	private static final long serialVersionUID = -6340607844054298787L;

	public RegistroNoEncontradoException() {
	}

	public RegistroNoEncontradoException(String arg0) {
		super(arg0);
	}

	public RegistroNoEncontradoException(Throwable arg0) {
		super(arg0);
	}

	public RegistroNoEncontradoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
