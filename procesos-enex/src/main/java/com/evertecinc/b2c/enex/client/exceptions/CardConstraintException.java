package com.evertecinc.b2c.enex.client.exceptions;

public class CardConstraintException extends Exception {

	/**
	 * Serializable
	 */
	private static final long serialVersionUID = -6340607844054298787L;

	public CardConstraintException() {
	}

	public CardConstraintException(String arg0) {
		super(arg0);
	}

	public CardConstraintException(Throwable arg0) {
		super(arg0);
	}

	public CardConstraintException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
