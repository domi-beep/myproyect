package com.evertecinc.b2c.enex.client.exceptions;

public class VariableException extends Exception
{

	/**
	 * 
	 */
	private static final long serialVersionUID = 8347664980756471502L;
	
	public VariableException() {
	}

	public VariableException(String arg0) {
		super(arg0);
	}

	public VariableException(Throwable arg0) {
		super(arg0);
	}

	public VariableException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
