package com.evertecinc.b2c.enex.client.exceptions;

public class StationException extends Exception {
	
	
	 private static final long serialVersionUID = -6340607844054298787L;

		public StationException() {
		}

		public StationException(String arg0) {
			super(arg0);
		}

		public StationException(Throwable arg0) {
			super(arg0);
		}

		public StationException(String arg0, Throwable arg1) {
			super(arg0, arg1);
		}

}
