package com.evertecinc.b2c.enex.client.exceptions;

public class ProductException extends Exception {

	private static final long serialVersionUID = -6340607844054298787L;

	public ProductException() {
	}

	public ProductException(String arg0) {
		super(arg0);
	}

	public ProductException(Throwable arg0) {
		super(arg0);
	}

	public ProductException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
