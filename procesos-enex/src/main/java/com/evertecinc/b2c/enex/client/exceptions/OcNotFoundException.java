package com.evertecinc.b2c.enex.client.exceptions;

public class OcNotFoundException extends Exception{

	public OcNotFoundException() {
		super();
	}

	public OcNotFoundException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public OcNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public OcNotFoundException(String message) {
		super(message);
	}

	public OcNotFoundException(Throwable cause) {
		super(cause);
	}
	

}
